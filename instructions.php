<h3>Sales Do's and Don'ts</h3>

<p><b>DO</b></p>
<ul>
<li>Greet each customer when he or she arrives.</li>
<li>Offer to assist the customer in selecting a tree.  Measuring sticks are available, but
must be used carefully to avoid knocking down the power lines to the sales shed.</li>
<li>Remove trees that have been selected using a pry bar if needed.  Be sure to put a
small red cone over the exposed pin.</li>
<li>Bring the tree to the sales shed so that the customer can pay for the tree.</li>
<li>Offer to cut up to &#189;” off the bottom of the tree using a bow saw IF the customer
plans to put the tree in water within 4-6 hours.  Tree butts and branches should be put
in the appropriate barrel, NOT in the trash barrel.</li>
<li>Show the customer a tree bag and explain that it is put under the tree when the tree
is set up at home and is then pulled up around the tree to help dispose of the tree.  It
can also be used to protect the customer’s car if the tree is tied to the top.</li>
<li>Tie the tree to the top of the customer’s car using twine.  Have an adult
double-check to make sure that the tree is secure before the car leaves the lot.</li>
<li>Thank the customer for buying the tree from the Sudbury Boy Scouts.</li>
<li>Restock the sold trees.</li>
<li>Keep the area clean, neat, and orderly.</li>
<li>Be respectful and listen to the adults on the sales shift.</li>
</ul>

<p><b>DON’T</b></p>
<ul>
<li>Play games, roughhouse, handle knives, build fires, or other activities not related 
to the selling of trees.</li>
<li>Use foul language, yell or scream at anyone.</li>
<li>Climb on or play around the stacks of trees.</li>
<li>Use personal audio devices.</li>
<li>Use the heater unless it is necessary.  An Adult should start it. No burning of items
in the heater is allowed.</li>
<li>Use a chain saw. Only adults may use the chain saw and only if extra busy on
weekends.</li>
<li>Just sit there. Find something to do. Each shift is not very long. Work as if it is
a job or service.</li>
</ul>


<h3>Site/Cash Opening</h3>
<ul>
<li>Arrive 15 minutes before the scheduled sales start time.</li>
<li>Give the money envelopes for <b>ALL</b> shifts to an adult on the sales shift. Adult should
put envelopes that are not needed for the current shift into a wooden box in the shed. 
Point out the last minute sales reminders in the envelope for the first shift. <i>Troop
coordinator has the money envelopes.</i></li>
<li>Unlock the equipment boxes in the sales hut. <i>Troop coordinator has the
combination.</i></li>
<li>Remove the cones and signs from the entrance to the sales lot. Make sure all of the
sales signs are displayed properly. Two A-frame signs by road with one of them under
spotlight from corral fence.</li>
<li>Turn on the electrical power to the site by flipping the 4 switches in the sales shed.
<b>DO NOT MOVE ANY OF THE PLUGS FROM THEIR CURRENT LOCATIONS!</b></li>
<li>Make sure a trash bag is in the trash barrel <b>NOT</b> used for yard waste.  The trash
barrel for trunks and branches should not have a bag.</li>
<li>Hang the fire extinguisher on the hook in the hut.</li>
</ul>

<h3>Site/Cash Closing</h3>
<ul>
<li>Arrive 15 minutes before the scheduled sales stop time.</li>
<li>Make sure the site is clean and fully stocked.  If not, have the current crew clean
and stock the corral.</li>
<li>Collect the money envelopes for <b>ALL</b> shifts from an adult on the sales shift.
Bring the envelopes to the treasurer's home after closing. <i>Troop coordinator has the
address.</i></li>
<li>Put all saws, the fire extinguisher, and any other items of value into the lockable
equipment boxes in the hut and lock them with the combination locks.</li>
<li>Close the entrance to the site by placing cones and signs with sales hours across the
entrance to the sales hut and corral.  Do <b>NOT</b> block the driveway in any way.</li>
<li>Turn off the electrical power by flipping the 4 switches in the sales shed.
<b>DO NOT MOVE ANY OF THE PLUGS FROM THEIR CURRENT LOCATIONS!</b></li>
<li>Have the sales crew empty the tree debris barrel into the debris area. Remove and
take home or ask someone to take home the trash bag in the trash barrel.  Do <b>NOT</b>
put trash into the Sullivan Tire dumpster. Place the barrels upside down to keep snow and
rain from collecting.</li>
</ul>

